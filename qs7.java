
public class qs7 {

	public static void main(String[] args) {
          double a=50,b=100,r=0,s=90;
          char ch='+';
          switch(ch)
          {
          case '+': r=a+b;
          break;
          case '-': r=a-b;
          break;
          case '*': r=a*b;
          break;
          case '/': r=a/b;
          break;
          case '^': r=s*s;
          }
          System.out.println("The desired output is "+r);
	}

}
